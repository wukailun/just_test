function y = spower2(x, k, m)
if k <= m
    y = ((x > 5) & (x < 9)) .* (16 * (1 - ((x - 8) / 3) .^ 2) - 16 * (1 - ((x - 9) / 3) .^ 2)) + ...
        ((x <= 5) | ((x >= 9) & (x < 13))) .* (16 * (1 - ((x - 8) / 4) .^ 2) - 16 * (1 - ((x - 9) / 4) .^ 2));
else
    y = ((x > 5) & (x < 12)) .* (16 * (1 - ((x - 11) / 6) .^ 2) - 16 * (1 - ((x - 12) / 6) .^ 2)) + ...
        ((x <= 5) | ((x >= 12) & (x < 20))) .* (16 * (1 - ((x - 11) / 8) .^ 2) - 16 * (1 - ((x - 12) / 8) .^ 2));
end